import { NextResponse } from "next/server";
import prisma from "@/lib/prisma";
import { addCorsHeaders } from "@/lib/cors";
import { getCachedPresets, invalidate } from "@/lib/cache";
import { route } from "@/lib/api/handler";
import { CreatePresetInput } from "@/schemas/api/preset";

export const GET = route({
  auth: "dietitian",
  scope: "presets.list",
  handler: async ({ request, auth, log }) => {
    try {
      const mealType = request.nextUrl.searchParams.get("mealType");
      const presets = await getCachedPresets(auth.user!.id, mealType);
      return addCorsHeaders(NextResponse.json(presets));
    } catch (err) {
      log.error("list failed", err instanceof Error ? err.message : err);
      return addCorsHeaders(
        NextResponse.json(
          { error: "Preset'ler yüklenirken bir hata oluştu" },
          { status: 500 },
        ),
      );
    }
  },
});

export const POST = route({
  auth: "dietitian",
  schema: CreatePresetInput,
  scope: "presets.create",
  handler: async ({ body, auth, log }) => {
    try {
      const preset = await prisma.mealPreset.create({
        data: {
          name: body.name.trim(),
          mealType: body.mealType ?? null,
          isAutoGenerated: false,
          isActive: true,
          dietitianId: auth.user!.id,
          items: {
            create: body.items.map((item, idx) => ({
              besinName: item.besinName ?? item.besin ?? "",
              miktar: item.miktar,
              birim: item.birim,
              order: idx,
            })),
          },
        },
        include: { items: true },
      });
      invalidate.presets(auth.user!.id);
      return addCorsHeaders(NextResponse.json(preset, { status: 201 }));
    } catch (err) {
      log.error("create failed", err instanceof Error ? err.message : err);
      return addCorsHeaders(
        NextResponse.json(
          { error: "Preset oluşturulurken bir hata oluştu" },
          { status: 500 },
        ),
      );
    }
  },
});
